package cn.com.yitong.framework.core.vo;

/**
 * 页面标签多语言支持
 * 
 * @author yaoym
 * 
 */
public class MultLagg {
	private String PROP_NO;
	private String NAME_CN;
	private String NAME_EN;
	private String NAME_HK;

	public String getPROP_NO() {
		return PROP_NO;
	}

	public void setPROP_NO(String pROP_NO) {
		PROP_NO = pROP_NO;
	}

	public String getNAME_CN() {
		return NAME_CN;
	}

	public void setNAME_CN(String nAME_CN) {
		NAME_CN = nAME_CN;
	}

	public String getNAME_EN() {
		return NAME_EN;
	}

	public void setNAME_EN(String nAME_EN) {
		NAME_EN = nAME_EN;
	}

	public String getNAME_HK() {
		return NAME_HK;
	}

	public void setNAME_HK(String nAME_HK) {
		NAME_HK = nAME_HK;
	}

}
